#include "libft.h"


char	*ft_strrchr(const char *s, int c)
{
	int	j;

	j = ft_strlen(s) - 1;
	while (s[j])
	{
		if (s[j] == c)
			return ((char*)&s[j]);
		j--;
	}
	return (NULL);
}
